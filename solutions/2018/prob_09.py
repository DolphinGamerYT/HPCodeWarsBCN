entrada = input()


variable_soporte = 0
primera_vez = True
lsita_output = []

while entrada != '-1':
    print3d, drink = entrada.split()
    if(primera_vez == True):
        if drink == 'Y':
            lsita_output.append(round(float(print3d)*1.8, 2))
        elif drink == 'N':
            lsita_output.append(round(float(print3d)*2, 2))
        entrada = input()


for x in lsita_output:
    print(x)
